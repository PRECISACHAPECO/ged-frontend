        console.log('ðŸš€ ~ datas:', datas)
        // Verifica se o CNPJ Ã© vÃ¡lido se ele for envalido retorna erro e retorna
        const cnpjValidation = validationCNPJ(datas.fields.cnpj)
        if (!cnpjValidation) {
            setError('fields.cnpj', {
                type: 'required',
                message: 'CNPJ invÃ¡lido'
            })
            return
        }

        const data = {
            ...datas,
            usuarioID: loggedUnity.usuarioID,
            fields: {
                dataCadastro: formatDate(datas.dataCadastro, 'YYYY-MM-DD'),
                ...datas.fields
            }
        }

        delete data.cabecalhoRelatorioTitle
        delete data.cabecalhoRelatorio

        try {
            if (type === 'new') {
                await api.post(`${backRoute(staticUrl)}/new/insertData`, data).then(response => {
                    router.push(`${backRoute(staticUrl)}`) //? backRoute pra remover 'novo' da rota
                    setId(response.data)
                    toast.success(toastMessage.successNew)
                })
            } else if (type === 'edit') {
                await api.post(`${staticUrl}/updateData/${id}`, data)
                toast.success(toastMessage.successUpdate)
                setShowNewPassword(false)
                getData()
            }
        } catch (error) {
            if (error.response && error.response.status === 409) {
                toast.error(toastMessage.errorRepeated)
            } else {
                console.log(error)
            }
        }

